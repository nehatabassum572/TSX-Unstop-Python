# 4. Experiment with different arithmetic and logical operators in the interpreter.
print(15 + 3)   # Addition
print(15 - 3)   # Subtraction
print(15 * 3)   # Multiplication
print(15 / 2)   # Division (float result)
print(15 // 2)  # Floor division
print(15 % 2)   # Modulus
print(15 ** 2)  # Exponentiation

print(True and False)  # AND
print(True or False)   # OR
print(not True)        # NOT
